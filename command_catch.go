func (P Pokemon) GetInspect() string {
	inspect := ""
	inspect += fmt.Sprintf("Name: %s\r\n", P.Name)
	inspect += fmt.Sprintf("Height: %d\r\n", P.Height)
	inspect += fmt.Sprintf("Weight: %d\r\n", P.Weight)
	inspect += "Stats:\r\n"
	for stat := range P.Stats {
		inspect += fmt.Sprintf("  -%s: %d\r\n", P.Stats[stat].Stat.Name, P.Stats[stat].BaseStat)
	}
	inspect += "Types:\r\n"
	for typ := range P.Types {
		inspect += fmt.Sprintf("  - %s\r\n", P.Types[typ].Type.Name)
	}
	return inspect
}

func commandCatch(cfg *Config, cache *pokecache.Cache) error {
	if len(cfg.params) == 0 {
		fmt.Println("You throw a ball into the void and ponder your own existence.")
	} else {
		fmt.Printf("Throwing a Pokeball at %s...\n", cfg.params[0])
		if pokemon, ok := getPokemon(cfg.params[0], cache); ok {
			chance := float64(rand.Intn(pokemon.BaseExperience)) / float64(pokemon.BaseExperience)
			if chance > 0.6 {
				fmt.Printf("%s was caught!\n", cfg.params[0])
				cfg.pokedex[cfg.params[0]] = pokemon
				fmt.Println("You may now inspect it with the inspect command.")
			} else {
				fmt.Printf("%s escaped!\n", cfg.params[0])
			}
		}
	}
	return nil
}

func getPokemon(name string, cache *pokecache.Cache) (Pokemon, bool) {
	area := Pokemon{}
	url := fmt.Sprintf("https://pokeapi.co/api/v2/pokemon/%v/", name)
	if val, ok := cache.Get(url); ok {
		//fmt.Println("Used cached value")
		if err := json.Unmarshal(val, &area); err != nil {
			log.Fatal(err)
		} else {
			return area, true
		}
	}
	resp, err := http.Get(url)
	if err != nil {
		log.Fatal(err)
	}
	defer resp.Body.Close()
	body, err := io.ReadAll(resp.Body)
	if err != nil {
		log.Fatal(err)
	}
	cache.Add(url, body)
	if err := json.Unmarshal(body, &area); err != nil {
		log.Fatal(err)
	}
	return area, true
}
